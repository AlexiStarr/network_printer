#!/bin/bash
# 6.4 可靠性自动化测试执行脚本

cd "$(dirname "$0")"

echo "============================================="
echo "  准备进行课题 6.4节: 系统可靠性验证与测试"
echo "============================================="

# 确保 MySQL 服务启动以供测试 1 的 Go 后端连接
if ! pgrep -x "mysqld" > /dev/null
then
    echo "[*] 启动 MySQL 数据库，为 Go 后端测试做准备..."
    brew services start mysql > /dev/null 2>&1
    sleep 2
fi

echo "[*] 开始执行可靠性整合测试集..."
go run test_6_4_reliability.go

if [ $? -eq 0 ]; then
    echo "============================================="
    echo "  所有测试项完美通过！论文数据已自动就绪。"
    echo "============================================="
else
    echo "============================================="
    echo "  [x] 存在测试未通过项，请检查控制台错误日志。"
    echo "============================================="
fi